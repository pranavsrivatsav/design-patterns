export class GarageDoor {
    doorUp(): void {
      console.log("Garage door is going up");
    }
  
    doorDown(): void {
      console.log("Garage door is going down");
    }
  
    lightsOn(): void {
      console.log("Garage door lights are on");
    }
  
    lightsOff(): void {
      console.log("Garage door lights are off");
    }
  
    doorStop(): void {
      console.log("Garage door stopped");
    }
  }